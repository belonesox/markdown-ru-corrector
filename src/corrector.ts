import * as punycode from 'punycode';

export function fixMarkdownText(text: string): string {
    let txt = text;
    const hidden: string[] = [];
    const u = "\u00A0"; // Неразрывный пробел 

    function r(r1: RegExp, r2: string | ((substring: string, ...args: any[]) => string)): void {
        txt = txt.replace(r1, r2 as any); 
    }

    // --- Расшифровка Punycode ---
    // Ищем домены, содержащие xn--.
    // Регулярка захватывает весь домен (например, xn--80aswg.xn--p1ai или www.xn--d1abbgf6aiiy.xn--p1ai)
    txt = txt.replace(/\b((?:[a-zA-Z0-9-]+\.)*xn--[a-zA-Z0-9-]+(?:\.[a-zA-Z0-9-]+)*)\b/gi, (match) => {
        try {
            // punycode.toUnicode корректно обрабатывает домены с точками,
            // конвертируя только те части, которые начинаются с xn--
            return punycode.toUnicode(match);
        } catch (e) {
            // Если декодирование не удалось, возвращаем оригинал
            return match;
        }
    });

    function hide(re: RegExp): void {
        r(re, (s) => {
            hidden.push(s);
            return `\x01${hidden.length}\x02`; 
        });
    }

    // Прячем блоки, внутри которых не должна работать типографика 
    hide(/```[\s\S]*?```/g); // Код 
    hide(/`[^`\n]+`/g);      // Инлайн-код 
    
    // Прячем LaTeX-формулы 
    hide(/\$\$[\s\S]*?\$\$/g); // Многострочные (блочные) формулы
    hide(/\$[^$\n]+\$/g);      // Однострочные (инлайн) формулы

    // Прячем ссылки и изображения, чтобы не ломать атрибуты вроде =50%x34
    hide(/!?\[[^\]]*\]\([^)]+\)/g); 

    // Прячем маркеры списков (включая вложенные в цитаты >) 
    hide(/^[\s>]*-(?=[ \t]+)/gm);
    // Прячем горизонтальные линии (hr) 
    hide(/^[\s>]*([-*_][ \t]*){3,}$/gm);

    // Прячем HTML-теги, чтобы не ломать типографику в их атрибутах
    hide(/<[^>]+>/g);

    // --- MARKDOWN ЭВРИСТИКИ ---
    // Удаляем висячие пробелы в конце строк 
    r(/[ \t]+(\n|\r|$)/g, "$1"); 
    // Схлопываем множественные пустые строки
    r(/\n{3,}/g, "\n\n");
    
    // Чиним заголовки 
    r(/^(#+)(?![\s#])(.*)$/gm, "$1 $2");
    r(/^(#+)\s{2,}(.*)$/gm, "$1 $2");

    // --- РУССКАЯ ТИПОГРАФИКА ---
    // Кавычки и вложенные кавычки 
    r(/(^|[\s(\[{>])"/g, "$1«");
    r(/"($|[\s.,!?:;)\]}])/g, "»$1");
    r(/"/g, "»");
    while (/«[^»]*«/.test(txt)) {
        r(/«([^»]*)«([^»]*)»/g, "«$1„$2“");
    }

    // Тире и диапазоны 
    r(/\s+-{1,3}\s+/g, " — "); 
    r(/(\d)--(\d)/g, "$1—$2"); 

    // Сокращения 
    r(/(Т|т)\.\s?е\./g, "$1о есть");
    r(/(Т|т)\.\s?к\./g, "$1ак как");
    r(/(В|в)\sт\. ?ч\./g, "$1 том числе");
    r(/(И|и)\sт\.\s?д\./g, "$1" + u + "т." + u + "д.");
    r(/(И|и)\sт\.\s?п\./g, "$1" + u + "т." + u + "п.");

    // Пробелы перед процентами 
    r(/([^%\/\wА-Яа-яЁё]\d+?(?:[\.,]\d+?)?) ?([%‰])(?!-[А-Яа-яЁё])/g, "$1" + u + "$2"); 
    r(/(\d) ([%‰])(?=-[А-Яа-яЁё])/g, "$1$2"); 

    // --- ВОССТАНАВЛИВАЕМ СКРЫТЫЕ БЛОКИ ---
    while (hidden.length > 0) {
        const replacement = hidden.pop()!;
        const safeReplacement = replacement.replace(/\$/g, "$$$$");
        txt = txt.replace("\x01" + (hidden.length + 1) + "\x02", safeReplacement); 
    }

    return txt.replace(/\s+$/, '') + '\n';
}